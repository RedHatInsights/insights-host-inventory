#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
WORKTREES_DIR="$REPO_ROOT/worktrees"
MAX_SLOTS=8

# --- Utility Functions ---

sanitize_name() {
    echo "$1" | tr '[:upper:]' '[:lower:]' | sed 's|/|-|g; s|[^a-z0-9_-]||g'
}

find_free_slot() {
    local used_slots=()
    for env_file in "$WORKTREES_DIR"/*/.env; do
        [[ -f "$env_file" ]] || continue
        local db_port
        db_port=$(grep '^INVENTORY_DB_PORT=' "$env_file" | cut -d= -f2)
        if [[ -n "$db_port" ]]; then
            local slot=$(( (db_port - 5432) / 100 ))
            used_slots+=("$slot")
        fi
    done

    for slot in $(seq 1 $MAX_SLOTS); do
        local in_use=false
        for used in "${used_slots[@]+"${used_slots[@]}"}"; do
            if [[ "$used" == "$slot" ]]; then
                in_use=true
                break
            fi
        done
        if [[ "$in_use" == "false" ]]; then
            echo "$slot"
            return 0
        fi
    done

    return 1
}

generate_env() {
    local safe_name="$1"
    local slot="$2"
    local wt_dir="$WORKTREES_DIR/$safe_name"
    local offset=$((slot * 100))

    cat > "$wt_dir/.env" << EOF
# Worktree: $safe_name (slot $slot)
# Auto-generated by scripts/worktree.sh — do not edit manually
COMPOSE_PROJECT_NAME=hbi-$safe_name
HBI_PG_DATA=$HOME/.pg_data_$safe_name

# Host port mappings (slot $slot, offset +$offset)
INVENTORY_DB_PORT=$((5432 + offset))
HBI_WEB_PORT=$((8080 + offset))
HBI_KAFKA_PORT=$((9092 + offset))
HBI_KAFKA_INTERNAL_PORT=$((29092 + offset))
HBI_REDIS_PORT=$((6379 + offset))
HBI_UNLEASH_PORT=$((4242 + offset))
HBI_GRAFANA_PORT=$((3000 + offset))
HBI_TEMPO_OTLP_PORT=$((4318 + offset))
HBI_TEMPO_INTERNAL_PORT=$((3250 + offset))
HBI_MINIO_API_PORT=$((9099 + offset))
HBI_MINIO_CONSOLE_PORT=$((9991 + offset))
HBI_EXPORT_PORT=$((8001 + offset))
HBI_EXPORT_METRICS_PORT=$((9090 + offset))
HBI_EXPORT_PRIVATE_PORT=$((10010 + offset))
HBI_PROM_GW_PORT=$((9091 + offset))

# App/test configuration
PROMETHEUS_MULTIPROC_DIR=/tmp
BYPASS_RBAC=true
BYPASS_UNLEASH=true
BYPASS_KESSEL=true
APP_NAME=inventory
INVENTORY_DB_USER=insights
INVENTORY_DB_PASS=insights
INVENTORY_DB_HOST=localhost
INVENTORY_DB_NAME=insights
INVENTORY_DB_POOL_TIMEOUT=5
INVENTORY_DB_POOL_SIZE=5
INVENTORY_DB_SSL_MODE=""
INVENTORY_DB_SSL_CERT=""
UNLEASH_CACHE_DIR=./.unleash
UNLEASH_URL=http://localhost:$((4242 + offset))/api
KAFKA_EXPORT_SERVICE_TOPIC=platform.export.requests
EOF

    local main_env="$REPO_ROOT/.env"
    if [[ -f "$main_env" ]]; then
        local token
        token=$(grep '^UNLEASH_TOKEN=' "$main_env" | head -1)
        if [[ -n "$token" ]]; then
            echo "$token" >> "$wt_dir/.env"
        fi
    fi
}

resolve_worktree() {
    local name="${1:-}"
    if [[ -n "$name" ]]; then
        local wt_dir="$WORKTREES_DIR/$name"
        if [[ ! -d "$wt_dir" ]]; then
            echo "Error: Worktree '$name' not found at $wt_dir" >&2
            exit 1
        fi
        echo "$wt_dir"
    else
        local cwd
        cwd=$(pwd)
        if [[ "$cwd" == "$WORKTREES_DIR"/* ]]; then
            local relative="${cwd#"$WORKTREES_DIR"/}"
            local wt_name="${relative%%/*}"
            echo "$WORKTREES_DIR/$wt_name"
        else
            echo "Error: Not inside a worktree directory and no name provided" >&2
            exit 1
        fi
    fi
}

is_stack_running() {
    local wt_dir="$1"
    (
        cd "$wt_dir"
        podman compose -f dev.yml ps -q 2>/dev/null | grep -q .
    )
}

wait_for_db() {
    local db_port="$1"
    local max_attempts=30
    local attempt=0
    echo "Waiting for DB on port $db_port..."
    while ! pg_isready -h localhost -p "$db_port" -q 2>/dev/null; do
        attempt=$((attempt + 1))
        if [[ $attempt -ge $max_attempts ]]; then
            echo "Error: DB did not become healthy after $max_attempts attempts" >&2
            return 1
        fi
        sleep 2
    done
    echo "DB is ready."
}

run_migrations() {
    local wt_dir="$1"
    local db_port="$2"
    cd "$wt_dir"
    unset PIPENV_PIPFILE
    INVENTORY_DB_HOST=localhost \
    INVENTORY_DB_PORT="$db_port" \
    INVENTORY_DB_NAME=insights \
    INVENTORY_DB_USER=insights \
    INVENTORY_DB_PASS=insights \
    pipenv run make upgrade_db
}

